#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	char	*str;

	str = (char *) s;
	if (n > 0)
	{
		str[0] = c;
		while (--n > 0)
			str[n] = c;
	}
	return (str);
}
